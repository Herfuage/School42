#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*s2;

	s2 = (char*)malloc(ft_strlen(s1) + 1);
	if (s2 != NULL)
	{
		ft_strcpy(s2, s1);
	}
	return (s2);
}
