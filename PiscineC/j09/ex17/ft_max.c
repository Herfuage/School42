int		ft_max(int *tab, int length)
{
	int max;
	int i;

	max = tab[0];
	i = 1;
	while (i < length)
	{
		if (tab[i] > max)
		{
			max = tab[i];
		}
		i = i + 1;
	}
	return (max);
}
