char	*ft_strrev(char *str)
{
	int		n;
	int		i;
	int		temp;

	n = 0;
	while (str[n] != '\0')
	{
		n = n + 1;
	}
	i = 0;
	while (i < n / 2)
	{
		temp = str[i];
		str[i] = str[n - i - 1];
		str[n - i - 1] = temp;
		i = i + 1;
	}
	return (str);
}
